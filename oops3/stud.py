# 1. Has a (object relation/ instatance relation)

class Student:
    def set_id(self,id):
        self.id = id
    def setName(self,name):
        self.name = name
    def setCourse(self,course):
        self.course = course 

    def getId(self):
        return self.id
    
    def getName(self):
        return self.name

    def getCourse(self):
        return self.course
    
    