class Employee:
    #setters.....
    def set_eid(self,eid):
        self.eid = eid
    def set_ename(self,ename):
        self.ename=ename
    def set_edept(self,edept):
        self.edept = edept
    def set_esal(self,esal):
        self.esal = esal

    #getters....
    def get_eid(self):
        return self.eid 
    def get_ename(self):
        return self.ename
    def get_edept(self):
        return self.edept
    def get_esal(self):
        return self.esal